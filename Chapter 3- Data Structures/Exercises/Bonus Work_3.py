# Part 3 -- Reverse Function | Concatenating Lists | Sort Function

list_of_names = ["Archer","Atlas","Rhysand","Azriel","Cassian","Feyre","Sam"]

# Reverse Function
print("\n----Reverse Function-----")
list_of_names.reverse()
print(list_of_names)

# Concatenating Lists
print("\n----Concatening Lists-----")
age = ["35", "40", "16", "8"]
print(list_of_names + age)

# Sort Function
print("\n----Sort Function-----")
print(age)
age.sort()
print(age)
print()