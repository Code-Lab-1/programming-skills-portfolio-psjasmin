## Solutions for Chapter 2: Variables & Comments

# Exercise 3: Stripping Names
# Tidy up the code to make it easier to understand

name = " \tArcher Hale\n "
print("\nOriginal:")
print(name)

print("Using lstrip():")
print(name.lstrip())
print("\nUsing rstrip():")
print(name.rstrip())
print("\nUsing strip():")
print(name.strip())

print()