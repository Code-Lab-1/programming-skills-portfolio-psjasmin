## Solutions for Chapter 7: Functions

# Exercise 2: Favorite Book
# Write a function called favorite_book() that accepts one parameter, title.

def favorite_book(title):
    """Displays a message about my favorite book."""
    print(f"""\n//=================================================\\\\
|| One of my favorite books is {title}. ||
\\\=================================================//\n""")

favorite_book("You've Reached Sam")