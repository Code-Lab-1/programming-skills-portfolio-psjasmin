# Part 1 -- Dictionary Format | Printing keys and values

# DICTIONARY SYNTAX
print("\n-----Printing Dictionaries-----")
student = {
    "Name":"Princess", "Age":16, "Class":"Year 1", "Gender":"Female", "Location":"United Arab Emirates",   
}
print(student)

# Printing a value
print("\n-----Printing A Value-----")
print(student["Location"])

# Printing only the first value (key)
print("\n-----Printing Keys-----")
print(student.keys())

# Printing only the second value (value)
print("\n-----Printing Values-----")
print(student.values())
print()